package DigistalStack01;

public class FirstExercise {

    public static void main(String[] args) {
        int counter = 0;
        for(int i = 1; i <= 10; i++){


            for(int j = 1; j <= i; j++){
                counter++;
                System.out.print(counter + " ");
            }

            System.out.println();









        }
    }

}
